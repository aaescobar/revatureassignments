
public class Example {
	public static void main(String[] args) {
		//initial value
		int i = 200;
			//cast to a short
		short s = (short)i;
		System.out.println(s);
		
		//cast to double
		double d =(double)i;
		System.out.println(d);
		
		//cast int to byte
		byte b = (byte)i;
		System.out.println(b);
	}
}

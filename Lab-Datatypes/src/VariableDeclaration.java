
public class VariableDeclaration {
	public static void main(String[] args) {
	//boolean declarations
	boolean on = false;
	System.out.println(on);
	//short declaration
	short s = 16;
	System.out.println(s);
	//int declaration
	int i = 32;
	System.out.print(i);
	//float declaration "must use f at the end to eliminate error"
	float f = 3839.34839f;
	System.out.println(f);
	//long declaration must use l to eliminate error 
	long l = 4294967296l;
	System.out.println(l);
	//char datatype declaration
	char c = 'a';
	System.out.println(c);
	
	}
}
